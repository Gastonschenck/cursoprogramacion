#porcentajes de niños y niñas
print("calcular porcentajes de niñxs")
Ninas=int(input("ingrese la cantidad de niñas: "))
Ninos=int(input("ingrese la cantidad de niños: "))
print ("el porcentaje de niñas es del: ", ((Ninas/(Ninos+Ninas))*100),"%")
print ("el porcentaje de niños es del: ", ((Ninos/(Ninos+Ninas))*100),"%")
